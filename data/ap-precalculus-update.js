  function findUnitIndex(units, unitNumber) {
    var pattern = new RegExp("^unit\\s*" + unitNumber + "(?:\\s*:|\\b)", "i");
    return units.findIndex(function (unit) {
      return pattern.test(String(unit && unit.title || "").trim());
    });
  }

